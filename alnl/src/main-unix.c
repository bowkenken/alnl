/***************************************************************
* UNIX 用のメイン処理
***************************************************************/

#define	MAIN_UNIX_C
#include	"inc.h"

/***************************************************************
* メイン・ルーチン
* int argc : 引数の数
* char **argv : 引数のリスト
* return : 終了コード
***************************************************************/

int	main( int argc, char **argv )
{
	g_argc = argc;
	g_argv = argv;

	init_arg();
	chk_arg( argc, argv );

	init_game();

	game_main();

	return EXIT_FAILURE;
}

